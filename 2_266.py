import math

def max_ampere_force(mu_0, I, a):
    """
    Вычисляет максимальное значение силы Ампера на единицу длины провода.

    :param mu_0: магнитная постоянная (Гн/м)
    :param I: сила тока в проводах (А)
    :param a: расстояние между проводами (м)
    :return: сила Ампера на единицу длины (Н/м)
    """
    return (mu_0 * I**2) / (2 * math.pi * a)

# Данные задачи
mu_0 = 4 * math.pi * 1e-7  # магнитная постоянная, Гн/м
I = 10  # сила тока, А
a = 0.05  # расстояние между проводами, м

# Вычисление силы Ампера
force = max_ampere_force(mu_0, I, a)

print(f"Максимальная сила Ампера на единицу длины провода: {force:.2e} Н/м")
