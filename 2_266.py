import math

def max_ampere_force(mu_0, I, a):
    """
    Вычисляет максимальное значение силы Ампера на единицу длины провода.

    :param mu_0: магнитная постоянная (Гн/м)
    :param I: сила тока в проводах (А)
    :param a: расстояние между проводами (м)
    :return: сила Ампера на единицу длины (Н/м)
    """
    return (mu_0 * I**2) / (2 * math.pi * a)

# Данные задачи
mu_0 = 4 * math.pi * 1e-7  # магнитная постоянная, Гн/м
I = 10  # сила тока, А
a = 0.05  # расстояние между проводами, м

# Вычисление силы Ампера
force = max_ampere_force(mu_0, I, a)

# Вывод решения
print("Метод решения:")
print("1. Формула для силы Ампера между двумя параллельными проводами:")
print("   F/L = (μ₀ * I²) / (2 * π * a),")
print("   где:")
print("   - μ₀ - магнитная постоянная (4π × 10⁻⁷ Гн/м),")
print("   - I - сила тока в проводах (А),")
print("   - a - расстояние между проводами (м).")
print("\n2. Подстановка значений:")
print(f"   μ₀ = {mu_0:.2e} Гн/м,")
print(f"   I = {I} А,")
print(f"   a = {a} м.")
print("\n3. Вычисление силы Ампера:")
print(f"   F/L = ({mu_0:.2e} * {I}²) / (2 * π * {a})")
print(f"   F/L = ({mu_0:.2e} * {I**2}) / (2 * π * {a})")
print(f"   F/L = {mu_0 * I**2:.2e} / {2 * math.pi * a:.2e}")
print(f"   F/L = {force:.2e} Н/м")
print("\nРезультат:")
print(f"Максимальная сила Ампера на единицу длины провода: {force:.2e} Н/м")