def ampere_force(B1, B2, I, L):
    """
    Вычисляет силу Ампера для проводника в области неоднородного магнитного поля.

    :param B1: магнитная индукция слева от проводника (Тл)
    :param B2: магнитная индукция справа от проводника (Тл)
    :param I: сила тока в проводнике (А)
    :param L: длина проводника (м)
    :return: сила Ампера (Н)
    """
    # Сила Ампера пропорциональна разнице магнитной индукции
    delta_B = B2 - B1
    F = I * L * delta_B
    return F

# Заданные данные
B1 = 0.5  # Тл, магнитная индукция слева
B2 = 1.0  # Тл, магнитная индукция справа
I = 5     # А, сила тока в проводнике
L = 2     # м, длина проводника

# Вычисление силы Ампера
force = ampere_force(B1, B2, I, L)

# Вывод решения
print("Метод решения:")
print("1. Формула для силы Ампера в неоднородном магнитном поле:")
print("   F = I * L * ΔB,")
print("   где:")
print("   - I - сила тока в проводнике (А),")
print("   - L - длина проводника (м),")
print("   - ΔB - разница магнитной индукции (B2 - B1) (Тл).")
print("\n2. Подстановка значений:")
print(f"   B1 = {B1} Тл (магнитная индукция слева),")
print(f"   B2 = {B2} Тл (магнитная индукция справа),")
print(f"   I = {I} А (сила тока),")
print(f"   L = {L} м (длина проводника).")
print("\n3. Вычисление разницы магнитной индукции:")
print(f"   ΔB = B2 - B1 = {B2} - {B1} = {B2 - B1} Тл")
print("\n4. Вычисление силы Ампера:")
print(f"   F = I * L * ΔB = {I} * {L} * {B2 - B1}")
print(f"   F = {I * L * (B2 - B1):.2f} Н")
print("\nРезультат:")
print(f"Сила Ампера на проводник: {force:.2f} Н")